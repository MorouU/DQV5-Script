def color( s: str, t: int ):
    return "\033[%dm" % t + s + "\033[30m"

def get():
    print("\n" + color('[*]', 34) + color('-' * 16 + 'Get evil code' + '-' * 16, 36) + "\n")
    while True:
        try:
            strings = list(input("=>"))
            print("\n" + color('[+]', 32) + color('-' * 16 + 'Try to get evil code' + '-' * 16, 36) + "\n")
            for index in range(len(strings)):
                strings[index] = Fonts_dict[strings[index]].replace("&","%26")
            print("(" + ".".join(strings)+")")
            print("\n" + color('[*]', 34) + color('-' * 16 + 'end' + '-' * 16, 36) + "\n")

        except KeyboardInterrupt:
            print("exit")
            exit(0)
        except Exception as e:
            print(color('\n[-]', 31), "Get evil code failed...",e)
            pass

if __name__ == '__main__':
    get()
